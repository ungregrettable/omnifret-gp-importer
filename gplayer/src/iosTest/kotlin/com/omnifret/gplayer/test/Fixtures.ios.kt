package com.omnifret.gplayer.test

import kotlinx.cinterop.ExperimentalForeignApi
import kotlinx.cinterop.addressOf
import kotlinx.cinterop.usePinned
import platform.Foundation.NSData
import platform.Foundation.dataWithContentsOfFile
import platform.posix.memcpy

/**
 * Resolve test fixtures via [IOS_TEST_RESOURCES_PATH], a build-time
 * constant generated by `:gplayer:generateIosTestResourcePath`. Env
 * vars don't propagate from Gradle through `simctl spawn` to the
 * simulator process, so we bake the absolute staged path in at
 * compile time. iOS sim processes can read host filesystem paths
 * directly, so this works without bundling the resources inside the
 * test framework.
 */
@OptIn(ExperimentalForeignApi::class)
internal actual fun loadTestFixture(path: String): ByteArray {
    val full = "$IOS_TEST_RESOURCES_PATH/test-data/$path"
    val data = NSData.dataWithContentsOfFile(full)
        ?: error("fixture not found: $full")
    val length = data.length.toInt()
    val bytes = ByteArray(length)
    if (length > 0) {
        bytes.usePinned { pinned ->
            memcpy(pinned.addressOf(0), data.bytes, length.toULong())
        }
    }
    return bytes
}
